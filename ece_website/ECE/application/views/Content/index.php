	<h4 class="ui header">Abstract</h4>
	<div class="ui item">

		<p>Manual attendance taking and report generation has its limitations. It is well enough for 30-60 students but when it comes to taking attendance of students large in number, it is difficult. For taking attendance for a lecture, a conference, etc. roll calling and manual attendance system is a failure. Time waste over responses of students, waste of paper etc. are the disadvantages of manual attendance system. Moreover, the attendance report is also not generated on time. Attendance report which is circulated over NITR webmail is two months old. To overcome these non-optimal situations, it is necessary that we should use an automatic on-line attendance management system. Project is planned to be done in three phases:</p>
		
	</div>
